public class Ifelse {
    public static void main(String[] arg){
        int num=79;
        if(num <=100 && num>74) {
            System.out.println("A");
        }
        else if(num <=74 && num>64) {
            System.out.println("B");
        }
        else if(num <=64 && num>54) {
            System.out.println("C");
        }
        else if(num <=54 && num>44) {
            System.out.println("S");
        }
        else {
            System.out.println("F");
        }
    }
}
